public class Exercicio_7_VerificarPadariaAbertaFechada {
    public static void main(String[] args) {
        int horaAtual = 7;
        if (horaAtual >= 6 && horaAtual <= 20) {
            System.out.println("Aberta");
        } else {
            System.out.println("Fechada");
        }
    }
}
